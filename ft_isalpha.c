#include <stdio.h>
#include <stdbool.h>

int ft_isalpha(int c)
{
    return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

int main()
{
    printf("------> %d\n", ft_isalpha('-'));
}